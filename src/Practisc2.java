public class Practisc2 {
    public static void main(String[] args) {
        for (int j = 0; j < 10; j++) {
            System.out.print("(0," + j + ") ");
        }
        System.out.println();

        for (int i = 1; i < 9; i++) {
            System.out.println("(" + i + ",0)");
        }

        for (int j = 0; j < 10; j++) {
            System.out.print("(9," + j + ") ");
        }
        System.out.println();
    }
}
