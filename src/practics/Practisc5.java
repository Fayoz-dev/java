public class Practisc5 {
    public static void main(String[] args) {
        for (int j = 0; j < 100; j=j+10) {
            System.out.print(j+" ");
            for (int k = 1; k < 10; k++) {
                System.out.print(k+j+" ");
            }
            System.out.println();
        }
        System.out.println();

    }
}
